    private void BtnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCadastrarActionPerformed
        String nome = tfNome.getText();
        String RG = tfRG.getText();
        String nasc = tfNascimento.getText();
        String emailPrincipal = tfEmailPrincipal.getText();
        String emailSecundario = tfEmailSecundario.getText();
        String telefoneResid = tfTelefone1.getText();
        String telefoneCom = tfTelefone2.getText();
        String telefoneCel = tfTelefone3.getText();
        String EnderecoResid = tfEndereco1.getText();
        String EnderecoCom = tfEndereco2.getText();

        int RGInt = Integer.parseInt(RG);
        int telefoneResidInt = Integer.parseInt(telefoneResid);
        int telefoneComInt = Integer.parseInt(telefoneCom);
        int telefoneCelInt = Integer.parseInt(telefoneCel);
        
        Contato c = new Contato(nome, RGInt, nasc);
        
        c.getEmails().put("Email Principal", emailPrincipal);
        c.getEmails().put("Emaill Secundario", emailSecundario);
        c.getTelefones().put("Telefone Redisencial", telefoneResidInt);
        c.getTelefones().put("Telefone Comercial", telefoneComInt);
        c.getTelefones().put("Telefone Celular", telefoneCelInt);
        c.getEnderecos().put("Endereco Residencial", EnderecoResid);
        c.getEnderecos().put("Endereco Comercial", EnderecoCom);

        dados.cadastrarContato(c);
        labelContador.setText(dados.numerodecontatos());
        System.out.println(c);
    }//GEN-LAST:event_BtnCadastrarActionPerformed

